def mesh_kwargs(**kwargs):
    """
    Create a dataframe with all parameter combinations.

    >>> mesh_kwargs(scale=[0.02, 0.05], sigma=[0.03, 0.06])
       scale  sigma
    0   0.02   0.03
    1   0.05   0.03
    2   0.02   0.06
    3   0.05   0.06
    """

    scan_params = list(kwargs.keys())     # (npar,)
    # Number of scanned parameters (i.e. columns in final df)
    # npar = len(scan_params)

    mesh = np.meshgrid(*kwargs.values())  # npar × (nval1, nval2, ...)
    mshape = np.shape(mesh)               # (npar, nval1, nval2, ...)
    shape = (mshape[0], np.prod(mshape[1:]))  # (npar, nval1*nval2*...)
    df_params = pandas.DataFrame(
        np.reshape(mesh, shape).T,        # (nval1*nval2*..., npar)
        columns=scan_params)              # (npar,)

    return df_params


def get_sneia_simulation(size=10_000, zmax=1.5, **kwargs):
    """
    Get a dataframe of simulated Type Ia Supernovae (Tripp relation only)

    :param int size: number of simulated targets
    :param float zmax: maximal redshift
    :param kwargs: goes to SNeIa.from_draw (zmin, etc)
    :return: dataframe
    """

    from skysurvey import target

    sneia = target.SNeIa.from_draw(size=size, zmax=zmax, **kwargs)

    return sneia.data.copy()


def show_timescan(data,
                  ax=None, ystep=1, total_time=3,
                  rename_param={}, **kwargs):
    """
    Display the dataframe created by scan_totaltime

    :param pandas.DataFrame data: dataframe generated by
           :meth:`StudySample.scan_totaltime`
    :param ax: matplolib's axes
    :param float ystep: vertical offset between bars
    :param float total_time: total time (in year) of the mission
    :param dict rename_param: change the scanned parameter names.
    :param kwargs: goes to ax.barh
    :return: figure
    """

    from astropy import units
    from matplotlib.colors import to_rgba
    import matplotlib.pyplot as plt

    if ax is None:
        fig = plt.figure(figsize=[7, 4])
        ax = fig.add_axes([0.15, 0.2, 0.7, 0.7])
    else:
        fig = ax.figure

    ylabels = []
    yarray = []

#    data = data.copy().reset_index()
    for i, (index, data_) in enumerate(data.iterrows()):
        y = i*ystep
        yarray.append(y) # for set_yticks later on
        data_param = data_[data_.index[~data_.index.isin(["targets","targets_main","targets_overhead"])]
                          ].rename(rename_param)

        ylabels.append("\n".join([f"{k}:{v}" for k,v in data_param.iteritems()]))


        ax.barh(y, total_time, facecolor="None", edgecolor="0.7", zorder=5,
               **kwargs)
        ax.barh(y, total_time*0.75, facecolor="None", edgecolor="k", zorder=5,
               **kwargs)

        # data
        on_targets = data_["targets"] * units.second.to("year")
        target_main = data_["targets_main"] * units.second.to("year")
        overhead = data_["targets_overhead"]* units.second.to("year")-on_targets
        targets_top5 = on_targets - target_main

        # data main
        ax.barh(y, target_main, left=0,
                facecolor=to_rgba("C0",0.8), edgecolor=None,  zorder=3,
                label="main targets" if i==0 else "",
               **kwargs)
        # data to 5%
        ax.barh(y, targets_top5, left=target_main,
                facecolor=to_rgba("C0",1), edgecolor=None,  zorder=3,
                label="5% faintest" if i==0 else "",
               **kwargs)

        # data overhead
        ax.barh(y, overhead, left=on_targets,
                facecolor="tab:grey", edgecolor="k", zorder=3,
                label="overhead" if i==0 else "",
               **kwargs)

    clearwhich = ["left","right","top","bottom"]
    [ax.spines[which].set_visible(False) for which in clearwhich]
    ax.set_xticks([1,2,3])
    ax.set_yticks(yarray, labels=ylabels, fontsize="x-small")

    ax.legend(bbox_to_anchor=(0., 1.0, total_time/(total_time+0.1), .10), loc='lower left',
                              ncol=3, mode="expand",
            borderaxespad=0., fontsize="x-small", frameon=False)

    ax.set_xlim(0,total_time+0.1)
    ax.set_xlabel("Mission Time (yr)")
    return fig


class Study:
    """
    Explore MLAperf simulations.

    .. seealso::

       * :class:`StudyCase`: study single case
       * :class:`StudySample`: study samples
    """

    def __init__(self, simu):
        """
        Study a :class:`Simulation`.

        :param Simulation simu: simulation to be explored.
        """

        self.simu = simu

    @classmethod
    def from_config(cls, config):
        """
        Load the simulation given its config.

        This calls `Simulation.from_config(config)`.

        :param dict config: top level configuration containing
                            configurations for scene, spectrograph, detector
                            and extraction parameters (by default:
                            use default config from
                            :func:`mlaperf.iotools.get_config`)
        :return: scene instance
        """

        simu = Simulation.from_config(config)
        return cls(simu=simu)

    def update_simulation(self, **kwargs):
        """
        updates the simulation and returns the original config.
        """

        changed_parameters = list(kwargs.keys())
        if "abmag" in changed_parameters:
            changed_parameters[changed_parameters.index("abmag")] = "redshift"

        origin = self.simu.get_parameter( changed_parameters ) # origin parameters
        self.simu.update(**kwargs) # set the new config

        return origin

    def fetch_nramp(self, target_snr, lbda_range=[4000, 6800], frame="rest", statistic=np.nanmean,
                        min_ramps=1, max_ramps=50, **kwargs):
        """
        loop over the number of ramps to get the first that passes the target_snr.

        unlike reach_snr, this does not fit, but loops over the number of ramps.

        :param float target_snr: objective SNR
        :param lbda_range: (wmin, wmax) test wavelength range [Å] (or list of)
        :param str frame: wavelength frame ('obs' or 'rest')
        :param statistic: numpy function to apply on test domain
        :param int min_ramps: minimal number of ramp
        :param int max_ramps: maximal number of ramp
        :param kwargs: goes to update_simulation to locally change the simulation parameters.
        :return int: minimal number of ramps needed to reach the target snr

        """

        nramp_orig = self.simu.get_parameter( "nramp" )
        origin = self.update_simulation(**kwargs)

        nramp_ = max_ramps
        for nramp in np.arange(min_ramps, max_ramps+1):
            # Compute SNR
            self.simu.update(nramp=nramp)
            snr = self.simu.get_band_snr( lbda_range=lbda_range, frame=frame, statistic=statistic)
            if snr>=target_snr:
                nramp_ = nramp
                break

        self.simu.update(nramp=nramp_orig)
        self.simu.update(**origin) # set back origin
        return nramp_

    def reach_snr(self, target, free_param="nramp",
                      lbda_range=[4000, 6800], frame="rest", statistic=np.nanmean,
                      as_int=False, reset_param=True, bounds=None):
        """
        Optimize parameter to reach target SNR.

        :param float target_snr: objective SNR
        :param str free_param: name of the mutable parameter to be optimized
        :param lbda_range: (wmin, wmax) test wavelength range [Å] (or list of)
        :param str frame: wavelength frame ('obs' or 'rest')
        :param statistic: numpy function to apply on test domain
        :param bool as_int: get the fitted variable as an int (ceil)
        :param bool reset_param: reset the optimized parameter to its input value
        :param bounds: boundaries of the optimized parameters (e.g. `[0, None]`)
        """

        from iminuit import Minuit

        def _objfun(value):                     # Objective function
            self.simu.update(**{free_param: value})  # Update simulation
            snr = self.simu.get_band_snr(            # Compute SNR
                lbda_range=lbda_range, frame=frame, statistic=statistic)
            return (snr - target)**2

        input_param = self.simu.get_parameter(free_param)
        init_param = 2#

        m = Minuit(_objfun, value=init_param)

        # Boundaries, mostly to avoid negative ngroup / nramp
        if free_param in ["ngroup", "nramp"]:
            as_int = True
            if bounds is None:
                bounds = [1, None]

        if bounds is not None:
            m.limits["value"] = bounds

        m.migrad()              # Minimization
        if not m.valid:
            print(m)
            warnings.warn(f"Cannot reach target SNR by varying parameter {free_param!r}.")

        value = m.values["value"]
        if as_int:
            value = np.ceil(value).astype(int)

        if reset_param:         # Reset if needed
            self.simu.update(**{free_param: input_param})

        return value

    def plot_psfsize(self, guiding_psf=(0, 0.01, 0.02, 0.05), ax=None):
        """
        Plot total spatial PSF scale given diffraction and guiding errors.

        :param guiding_psf: list of guiding error (sigma) [arcsec]
        """

        import matplotlib.pyplot as plt
        from astropy import units as u

        lbda = self.simu.spectrograph.lbda * u.angstrom
        sigma = self.simu.spectrograph.get_spatial_sigma() * u.arcsec
        dext = self.simu.spectrograph.meta["mirror"]["diameter_ext"]

        if ax is None:
            fig, ax = plt.subplots()

        for sguiding in guiding_psf:
            ax.plot(lbda.to(u.um),
                    np.hypot(sigma.to(u.arcsec), sguiding * u.arcsec) * 1e3,
                    label=f"{sguiding}\"", ls="--")

        # baseline
        sguiding = self.simu.get_parameter("guiding_sigma")
        ax.plot(lbda.to(u.um), np.hypot(sigma.to(u.arcsec), sguiding * u.arcsec) * 1e3,
                label=f"baseline\n{sguiding}\"", ls="-", color="k")

        spaxel_scale = self.simu.get_parameter("spatial_scale") * 1e3  # [mas]
        ax.axhline(spaxel_scale, ls="-", zorder=1, color="0.6")
        ax.set(xlabel="Wavelength [µm]",
               ylabel="σ (optics + guiding) [mas]",
               title=f"Total PSF for a {dext} telescope")
        ax.grid()
        ax.legend(title="Guiding error")

        return ax


class StudyCase(Study):

    def scan_variance(self, target_snr=10, free_param="nramp",
                      lbda_range=[4000, 6800], frame="rest",
                      statistic=np.nanmean,
                      **kwargs):
        """
        Scan variance origin over mutable parameters for a given SNR.

        :param float target_snr: objective SNR
        :param str free_param: name of the mutable parameter to be optimized
        :param lbda_range: (wmin, wmax) test wavelength range [Å] (or list of)
        :param str frame: wavelength frame ('obs' or 'rest')
        :param statistic: numpy function to apply on test domain
        :param dict kwargs: dictionary for test parameters
                            (e.g. `redshift=[0.5, 1.0, 1.5]`)
        :return: resulting dataframe
        :rtype: pandas.DataFrame
        """

        df_params = mesh_kwargs(**kwargs)

        # SNR properties
        snr_prop = dict(lbda_range=lbda_range, frame=frame, statistic=statistic)

        # Loop over parameter sets
        subdfs = []
        for _, params in tqdm(df_params.iterrows(), total=df_params.shape[0]):
            # For each row, update the npar parameters...
            param_dict = params.to_dict()
            self.simu.update(**param_dict)
            # Get the value of free parameter that provides the target SNR
            param_snr = self.reach_snr(
                target_snr, free_param=free_param, as_int=True, reset_param=True,
                **snr_prop)
            # Update the parameter accordingly
            self.simu.update(**{free_param: param_snr})

            dict_variances = self.simu.estimate_variance_contribution(**snr_prop)
            dict_variances[free_param] = param_snr

            subdf = pandas.Series(dict_variances)
            subdfs.append(subdf)

        df_variances = pandas.concat(subdfs, axis=1).T
        return df_params.join(df_variances)

    def scan_snr(self, lbda_ranges=[4000, 6800], frame="rest",
                     statistic=np.nanmean, name_ranges=[],
                     **kwargs):
        """
        Scan SNR over mutable parameters and store stats in dataframe.

        :param list lbda_ranges: list of (wmin,wmax) wavelength domains [Å]
        :param str frame: wavelength frame ('obs' or 'rest')
        :param statistic: numpy function to apply on test domains
        :param list name_ranges: names of wavelength domains
                                 ('wmin-wmax' by default)
        :param dict kwargs: dictionary for test parameters
                            (e.g. `redshift=[0.5, 1.0, 1.5]`)
        :return: result dataframe
        :rtype: pandas.DataFrame
        """

        lbda_ranges = np.atleast_2d(lbda_ranges)
        assert lbda_ranges.ndim == 2 and lbda_ranges.shape[-1] == 2, \
            f"Invalid lbda_ranges {lbda_ranges!r}."
        # Number of spectral bands
        nband = len(lbda_ranges)

        if not name_ranges:     # ['wmin_wmax'] by default
            name_ranges = [ f"{wmin:.0f}-{wmax:.0f}"
                            for wmin, wmax in lbda_ranges ]

        df_params = mesh_kwargs(**kwargs)
        print(f"SNR scan: {nband} bands, {npar} parameters, {nvalue} rows")

        subdfs = []
        # Loop over parameter sets
        for _, params in tqdm(df_params.iterrows(), total=df_params.shape[0]):
            # For each row, update the npar parameters...
            param_dict = params.to_dict()
            self.simu.update(**param_dict)
            # ... simulate and get the variance and signal...
            sig, var = self.simu.get_band_signal(
                lbda_ranges, frame=frame, statistic=statistic)
            # ... and store in results
            subdf = pandas.DataFrame({**{"signal": sig,
                                         "variance": var,
                                         "SNR": sig / var**0.5,
                                         "band": name_ranges},
                                      **param_dict})
            subdfs.append(subdf)

        df = pandas.concat(subdfs).reset_index(drop=True)
        df.attrs = self.simu.meta    # Add meta-data

        return df

    def fetch_maglimit(self, precision=0.1, magrange=[10,20], nmd=None):
        """
        fetch the smallest magnitude that has no saturation pixel

        :param float precision: step between two scanned magnitudes
        :param magrange: range of magnitude to be scanned.
        :param tuple nmd: detector NMD parameters

        :return float: minimal magnitude that does not saturated
        """

        mags = np.arange(*magrange, precision)
        origin_mag = self.simu.get_parameter("ABmag")
        if nmd is not None:
            origin_nmd = self.simu.get_parameter("nmd")
            self.simu.update(nmd=nmd)

        # scan over the magnitude limits

        mag_sat = None
        for mag in mags:
            self.simu.update(ABmag=mag)
            with warnings.catch_warnings():
                warnings.simplefilter("ignore")
                data, _ = self.simu.get_detected_cube() # variance ignored

            if self.simu.detector.nsaturated_detpx == 0:
                mag_sat = mag
                break

        self.simu.update(ABmag=origin_mag) # go back to original magnitude
        if nmd is not None:
            self.simu.update(nmd=origin_nmd)

        return mag_sat  # last magnitude from the loop

    def get_saturation_stat(self, mags="9:17:0.1", nmd=(16,16,0),
                            lbda_range=[4000,6800], frame="obs", **kwargs):
        """
        Generate a dataframe with saturation information.

        :param mags: observed magnitudes to scan; if string, argument to `np.r_`
        :param tuple nmd: detector NMD parameters
        :param lbda_range: (wmin, wmax) test wavelength range [Å] (or list of)
        :param str frame: wavelength frame ('obs' or 'rest')
        :param kwargs: goes to get_band_snr

        :return: dataframe (index mags)
        """

        if type(mags) is str:
            mags = eval(f"np.r_[{mags}]")

        input_nmd = self.simu.get_parameter("nmd") # store input nmd
        self.simu.update(nmd=nmd, **kwargs)        # set the simulation nmd

        mag_info = []
        for mag in mags:
            self.simu.update(ABmag=mag)
            with warnings.catch_warnings():
                warnings.simplefilter("ignore")
                snr = self.simu.get_band_snr(lbda_range, frame=frame)

            mag_info.append({"mag":mag,
                            "snr": snr,
                            "nsaturated_detpx": self.simu.detector.nsaturated_detpx,
                            "nsaturated_specpx": self.simu.detector.nsaturated_specpx,
                            })

        self.simu.update(nmd=input_nmd)             # set back the input

        # mag_info as list then set_index simplify labeling of index
        data = pandas.DataFrame(mag_info).set_index("mag")
        return data

