    def show_targets_obstime(self, ax=None, color="C0", second_color="w",
                          target_fractions=[20,50, 80,90,95,99,100]):
        """
        Shows how many targets can be observed as a function of the fraction
        of total time.

        the 'nramp' parameter should be loaded. See load_nramp

        :param ax: matplolib's axes
        :param color: main figure color (matplotlib color)
        :param second_color: color of the line and text with the axes
        :param list target_fractions: fraction of target displayed
        :return: figure
        """

        import matplotlib.pyplot as plt
        from matplotlib.colors import to_rgba
        from astropy import units

        time_on_target = (self.data["nramp"] *
                          self.simu.detector.exposure_time).sort_values()

        if ax is None:
            fig = plt.figure(figsize=[6,4])
            ax = fig.add_axes([0.1,0.1,0.8,0.8])
        else:
            fig = ax.figure

        total = time_on_target.sum() # in second
        cumsum = time_on_target.cumsum().values / total


        ftargets = np.asarray(target_fractions)/100
        ftimes = np.percentile(cumsum, ftargets*100)

        ax.fill_between(np.linspace(0,1, len(time_on_target)), cumsum,
                        facecolor=to_rgba(color,0.9),
                        edgecolor=to_rgba(color,1), lw=2)
        prop = dict(lw=0.5)
        ax.hlines(ftimes, 0, ftargets, color=color, **prop)
        ax.vlines(ftargets, 0, ftimes, color=second_color, **prop)

        # Target labels
        [ax.text(t_, 0.01, f"{t_:.0%}",
                 rotation=90, color=second_color, fontsize="x-small",
                va="bottom", ha="right")
         for t_ in ftargets[:-1]]

        ax.text(1.01, 0, f"{len(time_on_target)} targets", rotation=90,
                va="bottom", ha="left", color=color,
                transform=ax.transAxes, weight="medium")

        # Tile labels
        [ax.text(0.01,f_, f"{f_:.0%}",
                 rotation=0, color=color, fontsize="x-small",
                va="bottom", ha="left")
         for f_ in ftimes]

        total_in_week = total * units.second.to("week")
        ax.text(1., 1., f"{total_in_week:.1f} weeks", rotation=0,
                va="bottom", ha="right", color=color,
                transform=ax.transAxes, weight="medium")

        ax.set_xlim(0, 1)
        ax.set_ylim(0, 1)

        clearwhich = ["left","right","top","bottom"]
        [ax.spines[which].set_visible(False) for which in clearwhich]
        ax.set_xticks([])
        ax.set_yticks([])

        ax.set_ylabel("Fraction of on-target time", fontsize="large")
        ax.set_xlabel("Fraction of targets", fontsize="large")
        return fig

    def show_timescan(self, data=None, ax=None, scanning={}, **kwargs):
        """
        Display the dataframe created by :meth:`scan_totaltime`.

        :param DataFrame data: dataframe generated by :meth:`scan_totaltime`.
            If `None`, this will automatically measure on given the
            scanning parameters.
        :param ax: matplotlib's ax
        :param dict scanning: if data is not given, this is the scanning properties.
        """

        if data is None:
            data = self.scan_totaltime(**scanning)

        return show_timescan(data, ax=ax, **kwargs)


