    def show_saturation_mla(self, mag=None, ax=None, nmd=None, figsize=5,
                                cmap="cividis", **kwargs):
        """
        Display the saturation on the mla.

        :param float mag: locally change the observed magnitude
        :param ax: matplotlib's axes
        :param list nmd: locally change (n, m, d)
        :param float figsize: height of the figure. width automatically adapted.
        :param cmap: entry for plt.get_cmap()
        :param kwargs: goes to ax.imshow()
        :return: figure
        """

        # Things to change.
        if mag is not None:
            origin_mag = self.simu.get_parameter("ABmag")
            self.simu.update(ABmag=mag)
        else:
            origin_mag = None
            mag = self.simu.get_parameter("ABmag")

        if nmd is not None:
            origin_nmd = self.simu.get_parameter("nmd")
            self.simu.update(nmd=nmd)
        else:
            origin_nmd = None
            nmd = self.simu.get_parameter("nmd")

        # Cube
        data, variance = self.simu.get_detected_cube() # variance ignored
        f_saturated_pixels = np.sum(np.isnan(data), axis=0)/data.shape[0]

        # Plot
        if ax is None:
            import matplotlib.pyplot as plt
            fig = plt.figure(figsize=[figsize,figsize*0.7/0.8])
            ax = fig.add_axes([0.1,0.1,0.7,0.8])
            cax = fig.add_axes([0.82,0.1,0.02,0.8])
        else:
            fig = ax.figure
            cax = None

        # Actual plotting
        sc = ax.imshow(f_saturated_pixels, cmap=cmap, origin="lower",
                        **kwargs)
        cbar = fig.colorbar(sc, cax=cax, ax=ax)

        cbar.set_label("fraction of staturated pixel")
        ax.set_yticks(np.arange(data.shape[1])-0.5)
        ax.set_xticks(np.arange(data.shape[2])-0.5)

        ax.set_yticklabels(["" for _ in ax.get_yticks()])
        ax.set_xticklabels(["" for _ in ax.get_xticks()])
        ax.grid(lw=0.5)
        ax.text(0,1, f"magnitude: {mag} | readout-mode: {nmd}", transform=ax.transAxes,
                    va="bottom", ha="left", color="0.5")

        # Switch back to original.
        if origin_nmd is not None:
            self.simu.update(nmd=origin_nmd)

        if origin_mag is not None:
            self.simu.update(ABmag=origin_mag)

        return fig

