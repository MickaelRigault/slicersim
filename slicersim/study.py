class StudySample(Study):

    def __init__(self, simu, data):
        """
        Study a sample.

        :param Simulation simu: simulation to be explored.
        :param DataFrame data: target data
        """

        super().__init__(simu=simu)
        self.data = data

    @classmethod
    def from_config(cls, config, size=5_000, **kwargs):
        """
        Load the sample given its config.

        This call `Simulation.from_config(config)`.

        :param dict config: top level configuration containing
                            configurations for scene, spectrograph, detector
                            and extraction parameters (by default:
                            use default config from
                            :func:`mlaperf.iotools.get_config`)
        :param int size: number of targets in the sample
        :param kwargs: goes to get_sneia_simulation (zmax, zmin, etc)
        :return: sample instance
        """

        from .simulation import Simulation
        simu = Simulation.from_config(config)
        data = get_sneia_simulation(size=size, **kwargs)
        return cls(simu=simu, data=data)

    def load_nramp(self, target_snr=10, force_reload=False, **kwargs):
        """
        update self.data with nramp parameters (using compute_nramp)

        :param float target_snr: objective SNR
        :param bool force_reload: is self.data already has nramp, shall the re-measure it ?
        :param kwargs: goes to the compute_nramp() method (zbins, use_dask etc)
        """

        if "nramp" in self.data and not force_reload:
            warnings("nramp already loaded. Set force_reload to true to reload")

        if "nramp" in self.data:
            _ = self.data.pop("nramp") # clean out
        self.data = self.compute_nramp(self.data, target_snr=10, **kwargs)

    def compute_nramp(self, data, target_snr=10,
                      zbins="0.001:1.5001:31j",
                      cbins="-0.3:0.5:9j",
                      x1bins="-4:4:9j",
                      use_dask=False, **kwargs
                     ):
        """
        Computes the nramp for each data row.

        :param DataFrame data: dataframe with columns 'z', 'c' and 'x1'.
        :param float target_snr: objective SNR
        :param zbins: redshift bining ; if string set `np.r_` otherwise array.
        :param cbins: color bining ; if string set `np.r_` otherwise array.
        :param x1bins: string bining ; if string set `np.r_` otherwise array.
        :param bool use_dask: shall this use dask (Client must be process based)
        :param kwargs: goes to fetch_nramp, update the simulation parameters.
        :return: copy of input data with additional bin and nramp columns.
        """

        if not np.all(np.in1d(["z","c","x1"],data.columns)):
            raise AttributeError("data must contain at least 'z','c' and 'x1'")

        data_ = data.copy()
        # binning
        if type(zbins) is str:
            zbins = eval(f"np.r_[{zbins}]")
        if type(cbins) is str:
            cbins = eval(f"np.r_[{cbins}]")
        if type(x1bins) is str:
            x1bins = eval(f"np.r_[{x1bins}]")

        z_bin = pandas.cut(data["z"], zbins, labels=np.arange(len(zbins[1:])))
        c_bin = pandas.cut(data["c"], cbins, labels=np.arange(len(cbins[1:])))
        x1_bin = pandas.cut(data["x1"], x1bins, labels=np.arange(len(x1bins[1:])))

        # format zbin_cbin_x1bin
        data_["bin"] = z_bin.astype("str")+"_"+c_bin.astype("str")+"_"+x1_bin.astype("str")
        binned = data_.groupby("bin")[["z", "c","x1"]].mean()

        # list of kwargs | rename as simu uses redshift not "z"
        updates = [d_.to_dict() for i, d_ in binned.rename({"z":"redshift"}, axis=1).iterrows()]

        if use_dask:
            import dask
            d_nramps = dask.delayed(list)([dask.delayed(self.fetch_nramp)(target_snr, **{**kwargs,**update_})
                                          for update_ in updates])
            nramps = d_nramps.compute() # list trick avoid requiring client as input
        else:
            nramps = [self.fetch_nramp(target_snr, **{**kwargs,**update_})
                      for update_ in updates]
        # merge
        binned["nramp"] = nramps
        dataout = data_.merge(binned["nramp"].reset_index(), on="bin")
        return dataout

    def get_total_exptime(self, overhead=None, ftargets=None):
        """
        compute the total observing time required (in sec)

        :param float overhead: extra time per target
        :param float ftargets: (0->1) fraction of brightest targets to observe
        :return float: total exposure time needed (in sec).
        """

        target_on_target = self.data["nramp"] * self.simu.detector.exposure_time
        # only take the brightest targets
        if ftargets is not None:
            target_on_target = target_on_target.sort_values()
            target_on_target = target_on_target.iloc[:int(len(target_on_target)*ftargets)]

        # extra time spent per target
        if overhead is not None:
            target_on_target +=overhead

        return target_on_target.sum()

    def scan_totaltime(self, zbins="0.001:1.5001:21j", cbins="-0.3:0.5:7j", x1bins="-4:4:7j",
                       highlight_ftargets=0.05, overhead=1000, use_dask=False,
                       **kwargs):
        """
        scan the total exposure time needed for this sample.

        :param zbins: redshift bining ; if string set `np.r_` otherwise array.
        :param cbins: color bining ; if string set `np.r_` otherwise array.
        :param x1bins: string bining ; if string set `np.r_` otherwise array.
        :param float highlight_ftargets: calcul the times needed for the top X percent.
        :param float overhead: observation overhead per target.
        :param kwargs: simulation parameter to scan {name:list, }
        :return: dataframe

        """

        df_params = mesh_kwargs(**kwargs)
        updates = [s_.to_dict() for i, s_ in df_params.iterrows()]
        orig = self.simu.get_parameter( list(kwargs.keys()) )
        dataout = []
        for update in updates:
            _ = self.update_simulation(**update)
            self.load_nramp(use_dask=use_dask, force_reload=True,
                           zbins=zbins, cbins=cbins, x1bins=x1bins)
            d_ = {"targets": self.get_total_exptime(),
                  "targets_main": self.get_total_exptime(ftargets=1-highlight_ftargets),
                  "targets_overhead":  self.get_total_exptime(overhead)
                 }
            dataout.append(d_)

        self.simu.update(**orig)
        return df_params.join(pandas.DataFrame(dataout))

